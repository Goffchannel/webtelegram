<?php

namespace Database\Seeders;

use App\Models\User;
use App\Models\Video;
use Illuminate\Database\Seeder;

/**
 * Imports 50 videos from the old project's PostgreSQL dump.
 *
 * Safe to run multiple times — skips videos whose telegram_file_id already exists.
 * All videos are assigned to the admin user with category_id = null.
 *
 * Run with: php artisan db:seed --class=ImportOldVideosSeeder
 */
class ImportOldVideosSeeder extends Seeder
{
    public function run(): void
    {
        $admin = User::where('is_admin', true)->first();

        if (! $admin) {
            $this->command->error('No admin user found. Create one first.');
            return;
        }

        $this->command->info("Assigning videos to admin: {$admin->name} (ID {$admin->id})");

        $videos = $this->videoData();

        $imported = 0;
        $skipped  = 0;

        foreach ($videos as $data) {
            if (Video::where('telegram_file_id', $data['telegram_file_id'])->exists()) {
                $skipped++;
                continue;
            }

            Video::create([
                'title'                  => $data['title'],
                'description'            => $data['description'],
                'price'                  => $data['price'],
                'telegram_file_id'       => $data['telegram_file_id'],
                'thumbnail_path'         => $data['thumbnail_path'],
                'thumbnail_blob_url'     => $data['thumbnail_blob_url'],
                'show_blurred_thumbnail' => $data['show_blurred_thumbnail'],
                'blur_intensity'         => $data['blur_intensity'],
                'allow_preview'          => true,
                'category_id'            => null,
                'creator_id'             => $admin->id,
                'product_type'           => 'video',
                'created_at'             => $data['created_at'],
                'updated_at'             => $data['updated_at'],
            ]);

            $imported++;
        }

        $this->command->info("Done. Imported: {$imported} | Skipped (already existed): {$skipped}");
    }

